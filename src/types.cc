#include "types.h"
#include <iostream>
#include <iomanip> // for std::setw

//========================================================================
// Image
//========================================================================
//------------------------------------------------------------------------
// print
//------------------------------------------------------------------------

void Image::print() const
{
  for ( size_t i = 0; i < n_rows; ++i ) {
    for ( size_t j = 0; j < n_cols; ++j ) {
      std::cout << std::setw(3) << std::right;
      std::cout << m_data[i*n_rows+j] << " ";
    }
    std::cout << std::endl;
  }
  std::cout << std::endl;
}

//========================================================================
// Label
//========================================================================
//------------------------------------------------------------------------
// print
//------------------------------------------------------------------------

void Label::print() const
{
  std::cout << "Label: " << m_label << std::endl;
}

//========================================================================
// LabeledImage
//========================================================================
//------------------------------------------------------------------------
// print
//------------------------------------------------------------------------

void LabeledImage::print() const
{
  m_label.print();
  m_image.print();
}

